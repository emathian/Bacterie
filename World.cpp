  World::World(Bacteria** population[], int width, int height, float diffusion){
    
    int i;
    int j;
    for(i=0; i<width; i++){
      for(j=0; j<height; j++){
        pop_[i][j]=population[i][j];
        a_[i][j]=50.0;
        b_[i][j]=0.0;
        c_[i][j]=0.0;
      }
    }
    W_ = width;
    H_ = height;
    D_ = diffusion;
  }
  
// ===========================================================================
//                                 Destructor
// ===========================================================================
  
  World::~World(){
    int i;
    for(i=0; i<W_; i++){
      delete[] pop_[i];
      delete[] a_[i];
      delete[] b_[i];
      delete[] c_[i];
    }
    delete pop_[];
    delete[] a_;
    delete[] b_;
    delete[] c_;
    delete D_ ;
    delete W_ ; 
    delete H_ ;
  }
